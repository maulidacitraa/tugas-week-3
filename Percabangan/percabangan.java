public class percabangan {
    void cekAngka(int x) {
        if (x > 0) {
            System.out.println(x + " adalah bilangan positif");
        } else if (x < 0) {
            System.out.println(x + " adalah bilangan negatif");
        } else {
            System.out.println("Angka adalah nol");
        }
    }
}
